public class Mercado {
    private String Nome;
    private int maca;
    private double precoM;
    private int laranja;
    private double precoL;

    public String getNome() {
        return Nome;
    }

    public void setNome(String nome) {
        Nome = nome;
    }

    public int getMaca() {
        return maca;
    }

    public void setMaca(int maca) {
        this.maca = maca;
    }

    public double getPrecoM() {
        return precoM;
    }

    public void setPrecoM(double precoM) {
        this.precoM = precoM;
    }

    public int getLaranja() {
        return laranja;
    }

    public void setLaranja(int laranja) {
        this.laranja = laranja;
    }

    public double getPrecoL() {
        return precoL;
    }

    public void setPrecoL(double precoL) {
        this.precoL = precoL;
    }
}
